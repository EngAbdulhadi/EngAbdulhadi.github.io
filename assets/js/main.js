function downloadCV() {
    var cvElement = document.getElementById("cv");
    var opt = {
        margin:       0.5,
        filename:     'ozgecmis.pdf',
        image:        { type: 'jpeg', quality: 0.98 },
        html2canvas:  { scale: 2 },
        jsPDF:        { unit: 'in', format: 'a4', orientation: 'portrait' }
    };
    html2pdf().set(opt).from(cvElement).save();
}